class FakeEdge
  def initialize(head, tail)
    @head = head
    @tail = tail
    head.add_edge(self)
    tail.add_edge(self)
  end

  def head
    @head
  end

  def tail
    @tail
  end
  
  def build(edge_building)
    @building = edge_building
  end

  def building
    @building
  end
end
